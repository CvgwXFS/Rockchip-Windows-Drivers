struct _arg_GO {
	UINT8 chan;
	UINT32 addr;
	unsigned ns;
};

static inline UINT32 _emit_GO(unsigned dry_run, UINT8 buf[],
	const struct _arg_GO* arg)
{
	UINT8 chan = arg->chan;
	UINT32 addr = arg->addr;
	unsigned ns = arg->ns;

	if (dry_run)
		return SZ_DMAGO;

	buf[0] = CMD_DMAGO;
	buf[0] |= (ns << 1);
	buf[1] = chan & 0x7;
	buf[2] = addr & 0xff;
	buf[3] = (addr >> 8) & 0xff;
	buf[4] = (addr >> 16) & 0xff;
	buf[5] = (addr >> 24) & 0xff;

	return SZ_DMAGO;
}