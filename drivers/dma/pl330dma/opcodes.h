static inline UINT32 _emit_LP(unsigned dry_run, UINT8 buf[],
	unsigned loop, UINT8 cnt)
{
	if (dry_run)
		return SZ_DMALP;

	buf[0] = CMD_DMALP;

	if (loop)
		buf[0] |= (1 << 1);

	cnt--; /* DMAC increments by 1 internally */
	buf[1] = cnt;

	PL330_DBGCMD_DUMP(SZ_DMALP, "\tDMALP_%c %u\n", loop ? '1' : '0', cnt);

	return SZ_DMALP;
}

struct _arg_LPEND {
	enum pl330_cond cond;
	BOOLEAN forever;
	unsigned loop;
	UINT8 bjump;
};

static inline UINT32 _emit_LPEND(unsigned dry_run, UINT8 buf[],
	const struct _arg_LPEND* arg)
{
	enum pl330_cond cond = arg->cond;
	BOOLEAN forever = arg->forever;
	unsigned loop = arg->loop;
	UINT8 bjump = arg->bjump;

	if (dry_run)
		return SZ_DMALPEND;

	buf[0] = CMD_DMALPEND;

	if (loop)
		buf[0] |= (1 << 2);

	if (!forever)
		buf[0] |= (1 << 4);

	if (cond == SINGLE)
		buf[0] |= (0 << 1) | (1 << 0);
	else if (cond == BURST)
		buf[0] |= (1 << 1) | (1 << 0);

	buf[1] = bjump;

	PL330_DBGCMD_DUMP(SZ_DMALPEND, "\tDMALP%s%c_%c bjmpto_%x\n",
		forever ? "FE" : "END",
		cond == SINGLE ? 'S' : (cond == BURST ? 'B' : 'A'),
		loop ? '1' : '0',
		bjump);

	return SZ_DMALPEND;
}

